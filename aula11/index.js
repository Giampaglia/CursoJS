/* 
Operadores aritmédicos
+ = Adição ou Concatenação
- = Substração
/ = Divisão
** = Potenciação  
% = Resto da divisão
*/

const num1 = 5;
const num2 = 10;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);

let contador = 1; 
contador ++;

console.log(contador);

let contadorNegativo = 1; 
contadorNegativo --;

console.log(contadorNegativo);


